public class Rectangle {
    public static void main(String[] args) {
        int length = 4;
        int width = 6;
        int area;

        area = length * width;

        System.out.println("Length comes out to: " + length);
        System.out.println("Width comes out to: " + width);
        System.out.println("Area of the rectangle is: " + area);
    }
}
