public class Forloop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

    // another example, this will only print even values between 0 and 10

    for (int i = 0; i <= 10; i = i + 2) {
        System.out.print(i);
    }
}
}

